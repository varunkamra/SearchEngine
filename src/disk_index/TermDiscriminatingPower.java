package disk_index;

public class TermDiscriminatingPower {
	private String term;

	private float discriminatingPower;

	public float getDiscriminatingPower() {
		return discriminatingPower;
	}

	public void setDiscriminatingPower(float discriminatingPower) {
		this.discriminatingPower = discriminatingPower;
	}

	public String getTerm() {
		return term;
	}

	public void setTerm(String term) {
		this.term = term;
	}

}
